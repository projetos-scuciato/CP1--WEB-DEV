// Operadores logicos
let valor1 = 10;
let valor2 = 20;

// Diferente
if (valor1 != valor2){
    console.log("Os valores são diferentes!");
}

// Igual
if (valor1 == valor2){
    console.log("Os valores são iguais!")
}

// Maior ou igual
if (valor1 >= valor2){
    console.log(`O valor1 ${valor1} é maior ou igual que o valor2 ${valor2}`)
} else {
    console.log(`O valor1 (${valor1}) é menor que o valor2 (${valor2})`)
}

